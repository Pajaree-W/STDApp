    private void jButton_RemoveCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RemoveCourseActionPerformed
         //when delete course score all delete by constraint 
        
        if(!jTextField_Course_ID.getText().equals(""))
        {
            String id = jTextField_Course_ID.getText();
            c.InsertUpdateDeleteCourse('d', id, null, null);
            ManageCourseForm.jTable1.setModel(new DefaultTableModel(null , new Object[]{"Id","Label","Teacher"}));
            c.fillCourseJtable(ManageCourseForm.jTable1);
            MainForm.jLabel_CrsCount.setText("Course Count : " + Integer.toString(MyFunction.countData("course")));
            jTextField_Course_ID.setText("");
            jTextField_CourseLabel.setText("");
            jTextField_Course_teacher.setText("");
                  
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No Course Selected.");
        }
        
        
        


    }//GEN-LAST:event_jButton_RemoveCourseActionPerformed

