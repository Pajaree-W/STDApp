    private void jButton_EditCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditCourseActionPerformed
        
            String id = jTextField_Course_ID.getText();
            String label = jTextField_CourseLabel.getText();
            String teacher = jTextField_Course_teacher.getText();
            c.InsertUpdateDeleteCourse('u', id, label, teacher);
            ManageCourseForm.jTable1.setModel(new DefaultTableModel(null , new Object[]{"Id","Label","Teacher"}));
            c.fillCourseJtable(ManageCourseForm.jTable1);
    }//GEN-LAST:event_jButton_EditCourseActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int index = jTable1.getSelectedRow();
        jTextField_Course_ID.setText(jTable1.getValueAt(index, 0).toString());
        jTextField_CourseLabel.setText(jTable1.getValueAt(index, 1).toString());
        jTextField_Course_teacher.setText(jTable1.getValueAt(index, 2).toString());
        
    }//GEN-LAST:event_jTable1MouseClicked

